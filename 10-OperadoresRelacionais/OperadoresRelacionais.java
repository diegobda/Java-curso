public class OperadoresRelacionais {

    public static void main(String[] args) {
        int d = 10;
        int k = 20;

        System.out.printf("d e igual a 10? %b\n", d == 10);
        System.out.printf("d e diferente de 10? %b\n", d != 10);
        System.out.printf("d e maior que k? %b\n", d > k);
        System.out.printf("d e maior igual a 10? %b\n", d >= 10);
        System.out.printf("d e menor que 10? %b\n", d < 10);
        System.out.printf("d e menor ou igual a k? %b\n", d <= k);
   
    }
}
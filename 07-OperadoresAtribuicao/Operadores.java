public class Operadores {

    public static void main(String[] args) {
        int num = 0;
    
        System.out.println("Valor original:" + num);
        num += 3;
        System.out.println("Valor original:" + num);
        num *= 4;
        System.out.println("Valor original:" + num);
        num /= 2;
        System.out.println("Valor original:" + num);
        num %= 2;
        System.out.println("Valor original:" + num);
    }
    
}